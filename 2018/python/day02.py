from collections import Counter
def p1(a):
	a = a.strip().split("\n")
	two, three = 0, 0
	for x in a:
		if 2 in Counter(x).values(): two += 1
		if 3 in Counter(x).values(): three += 1
	print("Part 1:", two * three)
def p2(a):
	a = a.strip().split("\n")
	for x in a:
		for y in a:
			if x != y:
				co = [x[i] for i in range(len(x)) if x[i] == y[i]]
				if len(co) == len(x) - 1:
					print("Part 2:", "".join(co))
					return
def solve(a):
	p1(a)
	p2(a)
